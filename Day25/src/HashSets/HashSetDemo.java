package HashSets;

import java.util.HashSet;

public class HashSetDemo {

	public static void main(String[] args) {
		HashSet<String> hs = new HashSet<String>();
		hs.add("Saurabh");
		hs.add("Saurabh");
		hs.add("Saurabh");
		System.out.println(hs);
		
		// Duplicates is not allow in Hash Set and set.
	}

}
